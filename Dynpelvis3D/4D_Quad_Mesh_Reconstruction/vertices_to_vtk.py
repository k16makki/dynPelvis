def initTxt(filename):
    try:
        os.remove(filename)
    except:
        pass

def save_vertices_as_vtk_file(surface_points, vtk_out):

    nbSurfacePts = len(surface_points[:,0])
    logging.info(surface_points)
    logging.info(nbSurfacePts)
    initTxt(vtk_out)
    vtk_file = open(vtk_out, "a")
    vtk_file.write("# vtk DataFile Version 3.0\n")
    vtk_file.write("vtk output\n")
    vtk_file.write("ASCII\n")
    vtk_file.write("DATASET POLYDATA\n")
    vtk_file.write("POINTS {} float\n".format(nbSurfacePts))
    for c in range(nbSurfacePts):
        vtk_file.write("{:.1f} {:.1f} {:.1f}\n".format(surface_points[c,0], surface_points[c,1], surface_points[c,2]))
    print(c)
    vtk_file.write("VERTICES {} {}\n".format(nbSurfacePts, nbSurfacePts*2))
    for c in range(nbSurfacePts):
        vtk_file.write("1 {}\n".format(c))


# The current folder should contain all the quad meshes generated by Instant Meshes
# This will output a set of vtk files containing the mesh vertices or the point to be tracked

subjectSet = glob.glob('./*.obj') 

subjectSet.sort()
print(subjectSet)

for subject in range(len(subjectSet)):
 
        
        subject_name = subjectSet[subject].split('/')[-1].split('.')[0]
 
        outfile_name = './'+subject_name+'.vtk'

        m = pymesh.load_mesh(subjectSet[subject])

        print(m.vertices.shape)

        save_vertices_as_vtk_file(m.vertices, outfile_name)



        

